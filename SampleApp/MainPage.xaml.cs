namespace SampleApp
{
    /// <summary>
    /// Пустая страница, которую можно использовать саму по себе или для перехода внутри фрейма.
    /// </summary>
    public sealed partial class MainPage : Page
    {
        public MainPage()
        {
            this.InitializeComponent();
        }

        private void nvSample_SelectionChanged(Microsoft.UI.Xaml.Controls.NavigationView sender, Microsoft.UI.Xaml.Controls.NavigationViewSelectionChangedEventArgs args)
        {
            if (DataContext is MainPageViewModel viewModel && args.SelectedItem is FrameworkElement element)
            {
                viewModel.OpenNavigationMenuItemCommand?.Execute(element.Tag);
            }
        }
    }
}
